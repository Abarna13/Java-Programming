
public class ShortHandOperaors {

	public static void main(String[] args) {
		
		//Short hand operators  
	    int i=78;
	    i += 3; //i = i+3
	    System.out.println(i);
	    
	    i = 65;
	    i -= 4;//i=i-4
	    System.out.println(i);
	    
	    i = 4;
	    i *= 9;//i=i*9
	    System.out.println(i);
	    
	    
	    i = 36;
	    i /= 6; //i = i/6
	    System.out.println(i);
	    
	    i = 38;
	    i %= 6; //i = i%6
	    System.out.println(i);
	    
	    
	    
	    
	    
	    

	}

}
