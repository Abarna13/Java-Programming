		double mynum4 = 19.99d; //8 bytes -> 64bits ->Sufficient for storing 15 decimal digits
		boolean isjavafun = true; //1 byte -> Stores true or false values
		char mygrade = 'B'; //2 bytes -> 16 bits ->Stores a single character/letter or ASCII values
		mygrade = 68;
		System.out.println(a);
		System.out.println(mynum);
		System.out.println(mynum1);
