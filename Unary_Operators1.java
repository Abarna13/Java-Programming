public class Unary_Operators1
{

	public static void main(String[] args)
	{
		int num = 6;
		num++; //post increment
		System.out.println(num);
		
		++num; //pre-increment
		System.out.println(num);
		
		
		num--; //post decrement
		System.out.println(num);
		
		--num; //pre-decrement
		System.out.println(num);
		

	}

}
