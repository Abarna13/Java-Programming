
public class Relational_Operator
{

	public static void main(String[] args)
	{
		int a = 6, b=9;
		System.out.println("a is " + a + " and b is " +b);
		
		System.out.println(a == b);
		
		System.out.println(a != b);
		
		System.out.println(a > b);
		
		System.out.println(a < b);
		
		System.out.println(a >= b);
		
		System.out.println(a <= b);
		
		


		

	}

}
